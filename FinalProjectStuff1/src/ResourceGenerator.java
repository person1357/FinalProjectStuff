
public class ResourceGenerator 
{
	private int resources;

	public ResourceGenerator()
	{
		resources = 0;
	}
	public void addResources(int r) 
	{
		resources += r;
	}
	public int getResources()
	{
		return resources;
	} 
	public String toString()
	{
		return Integer.toString(resources);
	}
}
